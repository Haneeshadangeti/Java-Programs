package Control_Constructors;

public class Program18_natural {
	void display(int n)//creating a method
	{
		int sum=0;//Initializing the sum variable
		for(int i=1;i<=n;i++)//giving loop for natural numbers
		{
			
			sum=sum+i;//calculating a sum of natural numbers
			
			
		}
		System.out.println("sum of natural numbers is:"+sum);//printing a message
	
	}

}

package For_Loop;
import java.util.Scanner;

public class For_Quotient {

	public static void main(String[] args) {
		Scanner scan=new Scanner(System.in);//creating object for scanner class
		System.out.println("enter a number");
		int n=scan.nextInt();//creating a variable
		
		for(int i=1;i>1;i++)//loop to print n numbers
		{
			n=n/2;//number will be divide by 2
			System.out.println(n);
			
		}

	}

}
